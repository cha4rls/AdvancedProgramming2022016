#Gathers three numbers from the user
num_1 = int(input("Please enter the first number: "))
num_2 = int(input("Please enter the second number: "))
num_3 = int(input("Please enter the third number: "))

#Finds the highest number in the three
highest = max(num_1, num_2, num_3)

#Prints the output
print(f"The largest number is {highest}!")